#ifndef _EMD_H
#define _EMD_H

#include <string.h>

/******************************************************************************
******************************************************************************/
int* malloc_int(size_t count) {
    return((int*)malloc(count * sizeof(int)));
}

/******************************************************************************
******************************************************************************/
void clear_int(int* arr, size_t count) {
    memset(arr, 0, count * sizeof(int));
}

/******************************************************************************
******************************************************************************/
float* malloc_float(size_t count) {
    return((float*)malloc(count * sizeof(float)));
}

/******************************************************************************
******************************************************************************/
void clear_float(float* arr, size_t count) {
    memset(arr, 0, count * sizeof(float));
}

/******************************************************************************
******************************************************************************/
void free_(void* mem) {
    free(mem);
}

#endif
